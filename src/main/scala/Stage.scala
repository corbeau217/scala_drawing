package Main

import java.awt.Graphics
import java.awt.Color

// ################################################################################
// ################################################################################

class Stage(w:Int,h:Int) {
  
  // ###############################################################
  // ###############################################################

  // ....
  val width = w
  val height = h

  // ###############################################################
  // ###############################################################

  val bgColor = Color.BLACK

  // ###############################################################
  // ###############################################################

  

  // ###############################################################
  // ###############################################################

  def paint(g:Graphics):Unit = {
    g.setColor(bgColor)
    g.fillRect(0,0,w,h)
    //...
    g.setColor(Color.WHITE)
    g.drawOval(w/2,h/2,w/4,h/4)
  }

  // ###############################################################
  // ###############################################################

}

