package Main

import java.awt.Graphics
import java.awt.Color

// ################################################################################
// ################################################################################

object ConstLib {
  val scaleFactor = 0.02f
}

// ################################################################################
// ################################################################################

class Stage(w:Int,h:Int) {
  
  // ###############################################################
  // ###############################################################

  // ....
  val width = w
  val height = h

  val widthCenter = width/2.0
  val heightCenter = height/2.0


  // ###############################################################
  // ###############################################################

  val bgColor = Color.BLACK

  // ###############################################################
  // ###############################################################

  def paint(g:Graphics):Unit = {
    // space bg
    g.setColor(bgColor)
    g.fillRect(0,0,width,height)

    // planets/moons

    BodyLib.kerbin.paint( g,
      widthCenter,
      heightCenter
    )

    BodyLib.mun.paint( g,
      (widthCenter + (ConstLib.scaleFactor * 12000.0)),
      heightCenter
    )
  }

  // ###############################################################
  // ###############################################################

}
